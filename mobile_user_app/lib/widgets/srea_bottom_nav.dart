  static bool _isPointInPolygon(LatLng point, List<LatLng> polygon) {
    bool inside = false;
    for (int i = 0, j = polygon.length - 1; i < polygon.length; j = i++) {
      final xi = polygon[i].latitude;
      final yi = polygon[i].longitude;
      final xj = polygon[j].latitude;
      final yj = polygon[j].longitude;
      final intersect =
          ((yi > point.longitude) != (yj > point.longitude)) &&
          (point.latitude <
              (xj - xi) * (point.longitude - yi) / (yj - yi) + xi);
      if (intersect) inside = !inside;
    }
    return inside;
  }

  static String? _findBarangayByCoordinates(LatLng point) {
    for (int i = 0; i < _barangayPolygons.length; i++) {
      if (_isPointInPolygon(point, _barangayPolygons[i])) {
        return _barangayOptions[i];
      }
    }
    return null;
  }

