class SreaBottomNav extends StatelessWidget {
  final int currentIndex;
  final void Function(int) onTap;

  const SreaBottomNav({
    super.key,
    required this.currentIndex,
    required this.onTap,
  });

  static const List<_NavItemData> _items = [
    _NavItemData(icon: Icons.home_outlined, activeIcon: Icons.home_rounded),
    _NavItemData(
      icon: Icons.campaign_outlined,
      activeIcon: Icons.campaign_rounded,
    ),
    _NavItemData(
      icon: Icons.traffic_outlined,
      activeIcon: Icons.traffic_rounded,
    ),
    _NavItemData(
      icon: Icons.person_outline_rounded,
      activeIcon: Icons.person_rounded,
    ),
  ];

  @override
  Widget build(BuildContext context) {
    return BottomAppBar(
      color: SreaColors.primary,
      shape: const CircularNotchedRectangle(),
      notchMargin: 6,
      elevation: 6,
      padding: EdgeInsets.zero,
      child: SizedBox(
        height: 56,
        child: Row(
          mainAxisAlignment: MainAxisAlignment.spaceAround,
          children: [
            Expanded(
              child: Row(
                mainAxisAlignment: MainAxisAlignment.spaceEvenly,
                children: [0, 1]
                    .map(
                      (i) => _NavButton(
                        data: _items[i],
                        isActive: currentIndex == i,
                        onTap: () => onTap(i),
                      ),
                    )
                    .toList(),
              ),
            ),
            const SizedBox(width: 60),
            Expanded(
              child: Row(
                mainAxisAlignment: MainAxisAlignment.spaceEvenly,
                children: [2, 3]
                    .map(
                      (i) => _NavButton(
                        data: _items[i],
                        isActive: currentIndex == i,
                        onTap: () => onTap(i),
                      ),
                    )
                    .toList(),
              ),
            ),
          ],
        ),
      ),
    );
  }
}

class _NavItemData {
  final IconData icon;
  final IconData activeIcon;
  const _NavItemData({required this.icon, required this.activeIcon});
}

class _NavButton extends StatelessWidget {
  final _NavItemData data;
  final bool isActive;
  final VoidCallback onTap;

  const _NavButton({
    required this.data,
    required this.isActive,
    required this.onTap,
  });

  @override
  Widget build(BuildContext context) {
    return GestureDetector(
      onTap: onTap,
      behavior: HitTestBehavior.opaque,
      child: SizedBox(
        width: 56,
        child: Column(
          mainAxisSize: MainAxisSize.min,
          mainAxisAlignment: MainAxisAlignment.center,
          children: [
            Icon(
              isActive ? data.activeIcon : data.icon,
              color: isActive
                  ? SreaColors.bottomNavActive
                  : SreaColors.bottomNavInactive,
              size: 26,
            ),
            const SizedBox(height: 2),
          ],
        ),
      ),
    );
  }
}

